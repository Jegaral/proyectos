# Entero (int)
mi_entero = 10
# Flotante (float)
mi_flotante = (3.14)
# Cadena (str)
mi_cadena = "Hola, soy una cadena"
# Booleano (bool)
mi_booleano = True

# Definir el número 'n' hasta el cual queremos sumar los números pares
n = 10
# Inicializar una variable para almacenar el resultado de la suma
resultado = 0
# Bucle para sumar los números pares hasta 'n'
for i in range(2, n+1, 2):
    resultado += i
print("La suma de los primeros números pares hasta", n, "es:", resultado)
print(mi_entero)
print(mi_flotante)
print(mi_cadena)
print(mi_booleano)